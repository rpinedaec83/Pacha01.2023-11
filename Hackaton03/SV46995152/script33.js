/*Hacer un algoritmo en JavaScript que permita al usuario 
continuar con el programa. */
function ejercicio33(){

    
    // Se lee un número
    var numero = parseInt(prompt("Presione cualquier número para continuar: "));
    
  
    // Si el número es mayor o igual a 1, se continúa con el programa
    if (numero >= 1) {
      alert("El programa continúa");

    }else{
      alert("El programa no continúa");
    }
  
    // De lo contrario, se vuelve a pedir el número


        
      
      
      
      
}